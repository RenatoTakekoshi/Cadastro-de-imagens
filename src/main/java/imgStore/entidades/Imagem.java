package imgStore.entidades;

public class Imagem {
	
	int ID_IMG;
	String IMG_URL;
	String IMG_TITULO;
	
	
	public int getID_IMG() {
		return ID_IMG;
	}
	public void setID_IMG(int iD_IMG) {
		ID_IMG = iD_IMG;
	}
	public String getIMG_URL() {
		return IMG_URL;
	}
	public void setIMG_URL(String iMG_URL) {
		IMG_URL = iMG_URL;
	}
	public String getIMG_TITULO() {
		return IMG_TITULO;
	}
	public void setIMG_TITULO(String iMG_TITULO) {
		IMG_TITULO = iMG_TITULO;
	}
	
	

}
